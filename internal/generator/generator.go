func goType(t model.TypeRef) string {
	switch t.Kind {
	case model.TypePrimitive:
		return t.Primitive.Go
	case model.TypeEnum:
		return t.Name
	case model.TypeOptional:
		if t.Elem == nil {
			return "any"
		}
		return "*" + goType(*t.Elem)
	case model.TypeSlice:
		return t.Name
	case model.TypeString:
		return "string"
	case model.TypeBytes:
		return "[]byte"
	case model.TypeArray:
		if t.Elem == nil {
			return t.Raw
		}
		if t.Alias != "" {
			return t.Alias
		}
		return fmt.Sprintf("[%d]%s", t.ArrayLen, goType(*t.Elem))
	case model.TypeStruct:
		return t.Name
	case model.TypeVoid:
		return "struct{}"
	default:
		return ""
	}
}

