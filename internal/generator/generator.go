func refExpr(t model.TypeRef, expr string) string {
	switch t.Kind {
	case model.TypePrimitive:
		if t.Name == "bool" {
			return "_go2zigBool(" + expr + ")"
		}
		return abiType(t) + "(" + expr + ")"
	case model.TypeEnum:
		return abiType(t) + "(" + expr + ")"
	case model.TypeString:
		return "_go2zigRefString(" + expr + ")"
	case model.TypeBytes:
		return "_go2zigRefBytes(" + expr + ")"
	case model.TypeGoReader:
		return expr + ".handle()"
	case model.TypeGoWriter:
		return expr + ".handle()"
	case model.TypeOptional:
		return optionalRefFuncName(t) + "(" + expr + ")"
	case model.TypeSlice:
		return refSliceFuncName(t.Name) + "(" + expr + ")"
	case model.TypeArray:
		return arrayRefFuncName(t) + "(" + expr + ")"
	case model.TypeStruct:
		return refFuncName(t.Name) + "(" + expr + ").value"
	default:
		return expr
	}
}

