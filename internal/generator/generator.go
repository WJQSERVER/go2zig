func abiZeroValue(t model.TypeRef) string {
	switch t.Kind {
	case model.TypePrimitive:
		return abiType(t) + "(0)"
	case model.TypeEnum:
		return abiType(t) + "(0)"
	case model.TypeArray:
		return abiType(t) + "{}"
	default:
		return abiType(t) + "{}"
	}
}
